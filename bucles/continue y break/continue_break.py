# Instruccion continue y break

for i in range(0,10): #Continue continuar
    if i==5:
        continue #Continua al siguiente valor de la iteneracion.
    print(i)
    
print(' ')
print("Break")  
    

for i in range(10): #Break romper el bucle
    if i == 5:
        break #el break rompe el bucle
    print(i)
    
print("Fin")